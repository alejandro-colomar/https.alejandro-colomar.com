## Update repository stability status
## ==================================
##
## The stack name has a suffix to help identify it: "stable", "rc" or "exp".
##
################################################################################


################################################################################
##	source								      ##
################################################################################


################################################################################
##	definitions							      ##
################################################################################


################################################################################
##	functions							      ##
################################################################################
function update_stability()
{
	local	stability="$1";

	sed "/WWW_STABILITY=/s/\".*\"\;/\"${stability}\"\;/"		\
		-i ./etc/www/config.sh;
}


