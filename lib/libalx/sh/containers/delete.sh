function alx_stack_delete()
{
	local	mode="$1";	## "swarm", "kubernetes", or "openshift"
	local	stack="$2";

	case "${mode}" in
	"kubernetes")
		alx_kube_delete		"${stack}";
		;;
	"openshift")
		alx_oc_delete		"${stack}";
		;;
	"swarm")
		alx_swarm_delete	"${stack}";
		;;
	esac
}


