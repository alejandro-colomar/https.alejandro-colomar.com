.	etc/www/config.sh;


################################################################################
##	definitions							      ##
################################################################################
ARGC=1;

EX_USAGE=64;


################################################################################
##	functions							      ##
################################################################################


################################################################################
##	main								      ##
################################################################################
function main()
{
	local	mode="$1";
	local	project="${WWW_PROJECT}";
	local	stack="${WWW_STACK}";

	/usr/local/libexec/libalx/stack_deploy.sh	"${mode}" "${project}" "${stack}";
}


################################################################################
##	run								      ##
################################################################################
argc=$#;
if [ ${argc} -ne ${ARGC} ]; then
	echo >&2							\
'Usage: ./bin/containers/deploy mode
Mode:
	kubernetes
	openshift
	swarm';
	exit	${EX_USAGE};
fi

main	"$1";


